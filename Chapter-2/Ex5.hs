funca :: Double -> Double
funca x = x**(1/3)

funcb :: Double -> Double
funcb y = 2**y + 8**y

funcc :: Double -> Double
funcc x = 1/(sqrt((x-5)**2 + 16))

funcd :: Double -> Double
funcd x = 1/sqrt(1-x*x)

funce :: Double -> Double 
funce x = 1/(10+x) + 1/(10-x)

funcf :: Double -> Double
funcf x = sqrt(x*(x+1))

